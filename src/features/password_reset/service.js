import store from '@/store'
import auth from '@/auth/helpers'

export default class Service {
  constructor (options) {
    this.id = store.state.auth.user.id
  }

  sendResetPasswordMail (email, url) {
    return auth.post('/users/sendPasswordResetLink', {email: email, url: url})
      .then((response) => {
        return new Promise((resolve) => { resolve(response) })
      })
      .catch((error) => {
        return new Promise((resolve, reject) => { reject(error) })
      })
  }
  getUserByResetPasswordKey (k) {
    return auth.get('/users/getUserBySecretKey', {key: k})
      .then((response) => {
        return new Promise((resolve) => { resolve(response) })
      })
      .catch((error) => {
        return new Promise((resolve, reject) => { reject(error) })
      })
  }
  updatePassword (key, newPass) {
    return auth.put('/users/updateAccountPassword', {key: key, password: newPass})
      .then((response) => {
        return new Promise((resolve) => { resolve(response) })
      })
      .catch((error) => {
        return new Promise((resolve, reject) => { reject(error) })
      })
  }
}
